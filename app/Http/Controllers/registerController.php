     */
    public function store(Request $request)
    {
        $reg = new User();
        $reg->name = $request->name;
        $reg->email = $request->email;
        $reg->contact_no = $request->contact_no;
        $password = Hash::make($request->password); 
        $reg->password=$password;
        $reg->role = 2;
        $reg->save();
        return redirect('login');

    }

